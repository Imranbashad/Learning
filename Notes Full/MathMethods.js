// Math Methods with Explanations and Examples

// 1. Math.abs(x)
// Description: Returns the absolute value of a number.
console.log(Math.abs(-5)); // Output: 5

// 2. Math.acos(x)
// Description: Returns the arccosine (in radians) of a number.
console.log(Math.acos(0.5)); // Output: 1.0471975511965979

// 3. Math.acosh(x)
// Description: Returns the hyperbolic arccosine of a number.
console.log(Math.acosh(1)); // Output: 0

// 4. Math.asin(x)
// Description: Returns the arcsine (in radians) of a number.
console.log(Math.asin(0.5)); // Output: 0.5235987755982989

// 5. Math.asinh(x)
// Description: Returns the hyperbolic arcsine of a number.
console.log(Math.asinh(1)); // Output: 0.881373587019543

// 6. Math.atan(x)
// Description: Returns the arctangent (in radians) of a number.
console.log(Math.atan(1)); // Output: 0.7853981633974483

// 7. Math.atanh(x)
// Description: Returns the hyperbolic arctangent of a number.
console.log(Math.atanh(0.5)); // Output: 0.5493061443340548

// 8. Math.atan2(y, x)
// Description: Returns the arctangent (in radians) of the quotient of its arguments.
console.log(Math.atan2(1, 1)); // Output: 0.7853981633974483

// 9. Math.cbrt(x)
// Description: Returns the cube root of a number.
console.log(Math.cbrt(8)); // Output: 2

// 10. Math.ceil(x)
// Description: Returns the smallest integer greater than or equal to a given number.
console.log(Math.ceil(4.1)); // Output: 5

// 11. Math.clz32(x)
// Description: Returns the number of leading zero bits in the 32-bit binary representation of a number.
console.log(Math.clz32(1)); // Output: 31

// 12. Math.cos(x)
// Description: Returns the cosine of a number.
console.log(Math.cos(Math.PI / 3)); // Output: 0.5

// 13. Math.cosh(x)
// Description: Returns the hyperbolic cosine of a number.
console.log(Math.cosh(1)); // Output: 1.5430806348152437

// 14. Math.exp(x)
// Description: Returns `e` (Euler's number) raised to the power of a number.
console.log(Math.exp(1)); // Output: 2.718281828459045

// 15. Math.expm1(x)
// Description: Returns `e^x - 1`.
console.log(Math.expm1(1)); // Output: 1.718281828459045

// 16. Math.floor(x)
// Description: Returns the largest integer less than or equal to a given number.
console.log(Math.floor(4.9)); // Output: 4

// 17. Math.fround(x)
// Description: Returns the nearest 32-bit single precision float representation of a number.
console.log(Math.fround(1.337)); // Output: 1.3370000123977661

// 18. Math.hypot(...values)
// Description: Returns the square root of the sum of squares of its arguments.
console.log(Math.hypot(3, 4)); // Output: 5

// 19. Math.imul(a, b)
// Description: Returns the result of the 32-bit integer multiplication of its arguments.
console.log(Math.imul(2, 3)); // Output: 6

// 20. Math.log(x)
// Description: Returns the natural logarithm (base `e`) of a number.
console.log(Math.log(10)); // Output: 2.302585092994046

// 21. Math.log1p(x)
// Description: Returns the natural logarithm of `1 + x`.
console.log(Math.log1p(1)); // Output: 0.6931471805599453

// 22. Math.log10(x)
// Description: Returns the base 10 logarithm of a number.
console.log(Math.log10(100)); // Output: 2

// 23. Math.log2(x)
// Description: Returns the base 2 logarithm of a number.
console.log(Math.log2(8)); // Output: 3

// 24. Math.max(...values)
// Description: Returns the largest of zero or more numbers.
console.log(Math.max(1, 2, 3, 0.5)); // Output: 3

// 25. Math.min(...values)
// Description: Returns the smallest of zero or more numbers.
console.log(Math.min(1, 2, 3, 0.5)); // Output: 0.5

// 26. Math.pow(base, exponent)
// Description: Returns base raised to the power of exponent.
console.log(Math.pow(2, 3)); // Output: 8

// 27. Math.random()
// Description: Returns a pseudo-random number between 0 (inclusive) and 1 (exclusive).
console.log(Math.random()); // Output: A number between 0 and 1

// 28. Math.round(x)
// Description: Returns the value of a number rounded to the nearest integer.
console.log(Math.round(4.5)); // Output: 5

// 29. Math.sign(x)
// Description: Returns the sign of a number, indicating whether the number is positive, negative, or zero.
console.log(Math.sign(-3)); // Output: -1

// 30. Math.sin(x)
// Description: Returns the sine of a number.
console.log(Math.sin(Math.PI / 2)); // Output: 1

// 31. Math.sinh(x)
// Description: Returns the hyperbolic sine of a number.
console.log(Math.sinh(1)); // Output: 1.1752011936438014

// 32. Math.sqrt(x)
// Description: Returns the square root of a number.
console.log(Math.sqrt(16)); // Output: 4

// 33. Math.tan(x)
// Description: Returns the tangent of a number.
console.log(Math.tan(Math.PI / 4)); // Output: 1

// 34. Math.tanh(x)
// Description: Returns the hyperbolic tangent of a number.
console.log(Math.tanh(1)); // Output: 0.7615941559557649

// 35. Math.trunc(x)
// Description: Returns the integer part of a number by removing any fractional digits.
console.log(Math.trunc(4.9)); // Output: 4
